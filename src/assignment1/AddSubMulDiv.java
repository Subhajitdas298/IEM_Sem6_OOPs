package assignment1;

public class AddSubMulDiv {
    public static void main(String[] args) {
        int operand1 = 15, operand2 = 6;

        System.out.println(operand1 + " + " + operand2 + " : " + (operand1 + operand2));
        System.out.println(operand1 + " - " + operand2 + " : " + (operand1 - operand2));
        System.out.println(operand1 + " * " + operand2 + " : " + (operand1 * operand2));
        System.out.println(operand1 + " / " + operand2 + " : " + (operand1 / operand2));
    }
}
